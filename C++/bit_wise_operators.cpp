#include<iostream>
using namespace std;
int main()
 {
    int a=9;
    int b=7;
    cout<<(a&b)<<endl;
    cout<<(a|b)<<endl;
    cout<<(a^b)<<endl;
    cout<<(~a)<<endl;
    cout<<(~b)<<endl;
    cout<<(~a&~b)<<endl;
    cout<<(b<<1)<<endl;
    cout<<(b<<3)<<endl;
    cout<<(b>>2)<<endl;
    
    return 0;
 }